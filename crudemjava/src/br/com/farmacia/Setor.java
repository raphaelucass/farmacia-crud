package br.com.farmacia;

public class Setor {
	
	private int setor_id;
	private String nome_setor;
	private int qtdmed_setor;
	
	public int getSetor_id() {
		return setor_id;
	}
	public void setSetor_id(int setor_id) {
		this.setor_id = setor_id;
	}
	
	public String getNome_setor() {
		return nome_setor;
	}
	public void setNome_setor(String nome_setor) {
		this.nome_setor = nome_setor;
	}
	public int getQtdmed_setor() {
		return qtdmed_setor;
	}
	public void setQtdmed_setor(int qtdmed_setor) {
		this.qtdmed_setor = qtdmed_setor;
	}
}
